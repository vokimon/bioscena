void CCariotip::dump(CMissatger & msg)
{
	msg << "Cromosoma amb " << m_cromosomes.size() << " cromosomes " << endl;
	for (uint32 i=m_cromosomes.size(); i--;) {
		msg << "\t";
		m_cromosomes[i].dump(msg);
	}
}

